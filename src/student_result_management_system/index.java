        // TODO add your handling code here:
        String name = jTextField1.getText();
        String rollnumber = jTextField2.getText();
        String fathername = jTextField3.getText();
        String course = (String)jComboBox1.getSelectedItem();
        String branch = (String)jComboBox2.getSelectedItem();
        String gender = (String)jComboBox3.getSelectedItem();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Teja@123");
            
            Statement st = con.createStatement();
            st.executeUpdate("insert into student(rollno,name,fathername,coursename,branchname,gender) values('"+rollnumber+"','"+name+"','"+fathername+"','"+course+"','"+branch+"','"+gender+"')");
            JOptionPane.showMessageDialog(null, "Data Updated Succesfully");
            setVisible(false);
            new index().setVisible(true);
     
        }
