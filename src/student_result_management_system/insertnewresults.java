    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String maths = jTextField2.getText();
        String physics = jTextField3.getText();
        String chemistry = jTextField4.getText();
        String dbms = jTextField5.getText();
        String os = jTextField6.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Teja@123");
            
            Statement st = con.createStatement();
            
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,maths,physics,chemistry,dbms,os)values('"+rollno+"','"+maths+"','"+physics+"','"+chemistry+"','"+dbms+"','"+os+"')");
                JOptionPane.showMessageDialog(null,"Result is saved successfully.");
            }
            else{
                JOptionPane.showMessageDialog(null,"Student is not registered");
            }
        }
        catch(Exception e){
                JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

